from dataclasses import dataclass
from pathlib import Path
from typing import Optional, Any


@dataclass
class AppState:
    """
    Центральное состояние приложения.

    Этот класс НЕ должен заниматься интерфейсом.
    Его задача — хранить данные, которые нужны разным разделам приложения.

    Например:
    - PDF-раздел выделил картинку;
    - OCR-раздел получил текст;
    - Compare-раздел получил текст в левое или правое поле;
    - Excel-раздел открыл таблицу.

    Раньше эти данные хранились внутри разных окон.
    Теперь они будут храниться в одном месте.
    """

    # =========================================================
    # PDF
    # =========================================================

    # Путь к последнему открытому PDF-файлу.
    current_pdf_path: Optional[Path] = None

    # Номер текущей страницы PDF.
    # Сейчас у тебя используется только первая страница,
    # но поле пригодится, когда добавим переключение страниц.
    current_pdf_page: int = 0

    # Последняя выделенная область из PDF.
    # Тип Any используется, чтобы не привязывать AppState жёстко к PIL.
    # Фактически здесь будет лежать PIL.Image.
    selected_image: Optional[Any] = None

    # =========================================================
    # OCR
    # =========================================================

    # Последний распознанный текст.
    ocr_text: str = ""

    # Откуда был получен OCR-текст.
    # Например: "pdf_selection", "image_file" и т.д.
    ocr_source: str = ""

    # =========================================================
    # COMPARE
    # =========================================================

    # Текст в левом поле сравнения.
    compare_text_1: str = ""

    # Текст в правом поле сравнения.
    compare_text_2: str = ""

    # Источник текста в левом поле.
    # Например: "ocr", "excel", "manual".
    compare_text_1_source: str = ""

    # Источник текста в правом поле.
    compare_text_2_source: str = ""

    # =========================================================
    # EXCEL
    # =========================================================

    # Путь к последнему открытому Excel-файлу.
    current_excel_path: Optional[Path] = None

    # Название активного листа Excel.
    # Сейчас листы ещё не выбираются, но поле пригодится позже.
    current_excel_sheet: str = ""

    # Последняя выбранная ячейка Excel.
    current_excel_row: Optional[int] = None
    current_excel_col: Optional[int] = None
    current_excel_cell_value: str = ""

    # =========================================================
    # ОБЩЕЕ СОСТОЯНИЕ
    # =========================================================

    # Последний открытый раздел приложения.
    current_tab: str = "tab1"

    # Последнее сообщение статуса.
    # Можно использовать для строки состояния или логов.
    status_message: str = ""

    def reset_ocr(self):
        """
        Очищает данные OCR.

        Вызывается, если нужно сбросить распознанный текст
        и выделенную картинку.
        """
        self.selected_image = None
        self.ocr_text = ""
        self.ocr_source = ""

    def reset_compare(self):
        """
        Очищает данные сравнения.
        """
        self.compare_text_1 = ""
        self.compare_text_2 = ""
        self.compare_text_1_source = ""
        self.compare_text_2_source = ""

    def reset_excel_selection(self):
        """
        Очищает информацию о выбранной ячейке Excel.
        """
        self.current_excel_row = None
        self.current_excel_col = None
        self.current_excel_cell_value = ""

    def set_status(self, message: str):
        """
        Сохраняет последнее статусное сообщение.

        Пока это просто поле состояния.
        Позже можно будет связать его со строкой статуса в интерфейсе.
        """
        self.status_message = message